function shuffle(arr) {
	var curIndex = arr.length, temp, randIndex;

	while (0 !== curIndex) {

		randIndex = Math.floor(Math.rand() * curIndex);
		curIndex -= 1;

		temp = arr[curIndex];
		arr[curIndex] = arr[randIndex];
		arr[randIndex] = temp;
	}

	return arr;
}

